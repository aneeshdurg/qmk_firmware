bool llayer_lock = false;
bool rlayer_lock = false;
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        if(!llayer_lock) {
          layer_on(_LOWER);
        } else {
          llayer_lock = false;
          layer_off(_LOWER);
        }
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        if(!llayer_lock) {
          layer_off(_LOWER);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
        }
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        if(!rlayer_lock) {
          layer_on(_RAISE);
        } else {
          rlayer_lock = false;
          layer_off(_RAISE);
        }
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        if(!rlayer_lock) {
          layer_off(_RAISE);
          update_tri_layer(_LOWER, _RAISE, _ADJUST);
        }
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case LLYR_LK:
      if (record->event.pressed)
        llayer_lock = !llayer_lock;
      return false;
      break;
    case RLYR_LK:
      if (record->event.pressed)
        rlayer_lock = !rlayer_lock;
      return false;
      break;
}
  return true;
}
